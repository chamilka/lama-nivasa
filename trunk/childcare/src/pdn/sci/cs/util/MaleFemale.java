package pdn.sci.cs.util;

public class MaleFemale {

	private int male;
	private int female;

	public MaleFemale() {
		super();
	}

	public MaleFemale(int male, int female) {
		super();
		this.male = male;
		this.female = female;
	}

	public int getMale() {
		return male;
	}

	public void setMale(int male) {
		this.male = male;
	}

	public int getFemale() {
		return female;
	}

	public void setFemale(int female) {
		this.female = female;
	}

}
